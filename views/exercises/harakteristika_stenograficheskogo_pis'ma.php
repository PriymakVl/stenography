				<font color="#2E7344"><b>Глава I.</b></font></span><font color="#2E7344"><b>
				<span lang="ru">АЛФАВИТ И СЛИТНЫЕ ЗНАКИ<br>
				§1. Характеристика стенографического письма</span></b></font><span lang="ru"><br>
				<br>
				В стенографии для краткой записи используют элементы букв 
				обычного письма. Так, например, буквы Т и Н состоят из трех 
				элементов, а в стенографии каждый из них обозначает отдельную 
				букву, называемую знаком:</span></font><p style="margin-left: 20px; margin-right: 20px" align="center">
				<img border="0" src="../pic/img_003.jpg" width="500" height="258"><p style="margin-left: 20px; margin-right: 20px" align="justify">
				<span lang="ru"><font face="Arial" style="font-size: 11pt">
				Стенографические знаки стандартны. Выписывать их необходимо 
				точно, так как от изменения размера, формы или позиции знака 
				меняется его значение. Например:</font></span><p style="margin-left: 20px; margin-right: 20px" align="center">
				<img border="0" src="../pic/img_004.jpg" width="500" height="172"><p style="margin-left: 20px; margin-right: 20px" align="justify">
				<span lang="ru"><font face="Arial" style="font-size: 11pt">
				Стенографическое письмо должно быть легким, безнажимным и 
				безотрывным в пределах слова или словосочетания (фразеограммы) и 
				более наклонным, чем обычное. Наклон письма под Углом 45° 
				уменьшает искажение знаков при их соединении, помогает сохранить 
				соразмерность начертаний, содействует более точному выписыванию 
				острых углов.</font></span><p style="margin-left: 20px; margin-right: 20px" align="center">
				<img border="0" src="../pic/img_005.jpg" width="500" height="434"><p style="margin-left: 20px; margin-right: 20px" align="justify">
				<span lang="ru"><font face="Arial" style="font-size: 11pt">
				Точное выписывание графических элементов, из которых состоят 
				стенографические знаки, уменьшает деформацию их при быстрой 
				записи, облегчает и ускоряет процесс чтения. Чтобы выработать 
				такой почерк, нужно вначале писать очень медленно. Большую 
				пользу для приобретения правильного почерка приносят графические 
				упражнения.<br>
				<br>
				В начале обучения следует пользоваться тетрадями в две линейки. 
				Расстояние между линейками принимается за одну меру (одномерный 
				знак является ориентиром):</font></span><p style="margin-left: 20px; margin-right: 20px" align="center">
				<img border="0" src="../pic/img_006.jpg" width="500" height="185"><p style="margin-left: 20px; margin-right: 20px" align="justify">
				<span lang="ru"><font face="Arial" style="font-size: 11pt">
				Знаки, имеющие по размеру меньше одной меры, называются малыми, 
				все остальные (одномерные, двухмерные и трехмерные) — большими.<br>
				<br>
				<b>Упражнение 1 (графическое). Каждый пример напишите 
